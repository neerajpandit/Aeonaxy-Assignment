const updateAccountDetails = asyncHandler(async (req, res) => {
  const { firstName, lastName, phoneNo, email, gender } = req.body;

  if (!firstName && !lastName && !phoneNo && !email && !gender) {
    throw new ApiError(400, "fields are required");
  }

  const user = await User.findByIdAndUpdate(
    req.user?._id,
    {
      $set: {
        firstName: firstName,
        lastName: lastName,
        phoneNo: phoneNo,
        email: email,
        gender: gender,
      },
    },
    { new: true }
  ).select("-password");

  return res
    .status(200)
    .json(new ApiResponse(200, user, "Account details updated successfully"));
});

